
public class Literal {

	public static void main(String[] args) {
		System.out.print("Hello\nWorld");
		System.out.print("Hello\tWorld");
		System.out.print("Hello\rWorld");
		System.out.print("Hello\bWorld");
		System.out.print("\"Hello World\"");
		System.out.print("\\Hello World\\");
		System.out.print("\\\\Hello World\\\\");

	}

}
